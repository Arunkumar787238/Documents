

CREATE PROCEDURE PRCOURSEDELETE
(
   
    @FLDCOURSEID                BIGINT
   ,@ROWUSERCODE				INT

)
AS
BEGIN
        DELETE FROM TBLCOURSE
		 OUTPUT 2, @ROWUSERCODE, GETUTCDATE(), OBJECT_NAME(@@PROCID), NULL,DELETED.* INTO TBLAUDITCOURSE	   
		 WHERE FLDCOURSEID=@FLDCOURSEID
		   	END






	



	

	
     

	
		


		